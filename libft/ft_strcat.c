#include <string.h>
#include "libft.h"

char	*ft_strcat(char *restrict s1, const char *restrict s2)
{
	char *dst;
	char *src;

	dst = s1;
	src = (char *)s2;
	while (*dst)
		dst++;
	while (*src)
	{
		*dst = *src;
		dst++;
		src++;
	}
	*dst = '\0';
	return (s1);
}
